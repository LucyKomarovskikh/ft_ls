#include "libft.h"

int	ft_digitcount(int i)
{
	int d_count;

	d_count = 1;
	if (i == -2147483648)
		d_count = 0;
	if (i < 0 && i > -2147483648)
	{
		i = -i;
		d_count = 2;
	}
	while (i >= 9)
	{
		d_count++;
		i /= 10;
	}
	return (d_count);
}

#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	if (d < s)
		ft_memcpy(d, s, len);
	if (d > s)
		ft_memrcpy(d, s, len);
	return (dst);
}
